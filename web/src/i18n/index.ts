const DICTS: Record<Lang, Dict> = {
  'zh-TW': zhTW,
  'zh-CN': zhCN,
  'en-US': enUS,
  es,
  pt,
  fr,
  de,
  it,
  ja,
  ko,
}

export type Translate = (key: I18nKey, params?: Record<string, string | number>) => string

/** Resolve a key for a language, filling {placeholders} from params. */
export function translate(lang: Lang, key: I18nKey, params?: Record<string, string | number>): string {
  const dict = DICTS[lang] ?? zhTW
  let s = dict[key] ?? zhTW[key] ?? key
  if (params) {
    for (const p of Object.keys(params)) {
      s = s.split(`{${p}}`).join(String(params[p]))
    }
  }
  return s
}

const SUPPORTED = new Set<string>(LANGS.map((l) => l.code))

/** Best-effort initial language from the browser/OS, defaulting to zh-TW. */
export function detectLang(): Lang {
  if (typeof navigator === 'undefined') return 'zh-TW'
  const candidates = [navigator.language, ...(navigator.languages ?? [])]
  for (const raw of candidates) {
    if (!raw) continue
    const lc = raw.toLowerCase()
    if (lc === 'zh-tw' || lc === 'zh-hant' || lc.startsWith('zh-hant')) return 'zh-TW'
    if (lc.startsWith('zh')) return 'zh-CN'
    if (lc.startsWith('en')) return 'en-US'
    if (SUPPORTED.has(raw)) return raw as Lang
    const base = lc.split('-')[0]
    if (SUPPORTED.has(base)) return base as Lang
  }
  return 'zh-TW'
}
